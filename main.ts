// Example String Data Type
let personName: string = "Farheen";
console.log(personName);

//Example String Data Type with concatenation
let myName: string = "Farheen";
console.log("Hello " + myName + "!");


// Example Number Data Type
let age: number = 33;
console.log(age);

// Example Number Data Type with arithmetic operators
let x: number = 10
let y: number = 5
console.log(x+y); //Addition
console.log(x-y); //Subtraction
console.log(x*y); //Multiplication
console.log(x/y); //Division
console.log(x%y)
console.log(x**y)

// Example 3 :Boolean Data Type
let isStudent: boolean = true;
console.log(isStudent);

// Example Null Data Types
let b: null = null;
console.log(b);

// Example Undefined  Data Types
let c: undefined = undefined;
console.log(c);

let message; 
console.log(message);
